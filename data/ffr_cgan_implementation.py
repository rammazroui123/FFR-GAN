def generate_synthetic_ffr(n_samples=400, seed=42, mode='diameter'):
    """
    Generates synthetic FFR data based on hemodynamic physics.

    Parameters:
    - n_samples: Number of virtual patients
    - mode: 'diameter' (Real/Reference) or 'area' (Biased/Fake)
    """
    np.random.seed(seed)

    # 1. Generate Virtual Patient Geometries
    angles = np.random.choice([30, 45, 60, 90], n_samples)
    stenosis_ids = np.random.randint(1, 100, n_samples)

    # 2. Hemodynamic Modeling
    # FFR = 1 - (DeltaP / Pa)
    # DeltaP = C_lesion * (Stenosis^2) + C_vessel

    # Base stenosis severity (0.2 to 0.9)
    severity = np.random.uniform(0.2, 0.9, n_samples)

    if mode == 'diameter':
        # Realistic physics: Pressure drop is sensitive to diameter reduction
        pressure_drop = 0.5 * (severity**3.5) + 0.1 * (angles/90)
        ffr = 1.0 - pressure_drop
        # Add physiological noise
        ffr += np.random.normal(0, 0.02, n_samples)
    else:
        # Biased physics: Area-based assumptions underestimate pressure drop
        # This creates the "High FFR" bias seen in the fake dataset
        pressure_drop = 0.1 * (severity**2.0) + 0.02 * (angles/90)
        ffr = 1.0 - pressure_drop
        # Less variance in biased models
        ffr += np.random.normal(0, 0.01, n_samples)

    # 3. Final Constraints
    ffr = np.clip(ffr, 0.40, 1.0)

    # 4. Create DataFrame
    case_names = [f"A{a}_S{s:04d}" for a, s in zip(angles, stenosis_ids)]
    df = pd.DataFrame({
        'case_name': case_names,
        'FFR': ffr
    })

    return df

